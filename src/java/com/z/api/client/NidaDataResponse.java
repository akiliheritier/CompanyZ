package com.z.api.client;

/**
 *
 * @author DELL
 */
public class NidaDataResponse {
    private int code;
    private String status;
    private Data data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Data getData() {
        return data;
    }

    public void setData(Data data) {
        this.data = data;
    }

    @Override
    public String toString() {
        return "NidaDataResponse{" + "code=" + code + ", status=" + status + ", data=" + data + '}';
    }
    
}
